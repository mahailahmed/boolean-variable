// Declare a boolean variable
var isTrue = true;
var isFalse = false;
// You can also use the 'let' keyword without specifying the type explicitly
var isActive = false; // TypeScript infers the type as boolean
// Example of using boolean variables in a conditional statement
if (isActive) {
    console.log("The condition is true");
}
else {
    console.log("The condition is false");
}
